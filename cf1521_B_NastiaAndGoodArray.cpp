#include <iostream>
#include <vector>
#include <string>

using namespace std;

typedef long long int ll;

int main () {
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;

    vector<ll> a;
    for (size_t i = 0; i < n; i++) {
      int k; cin >> k;
      a.push_back(k);
    }

  }
  return 0;
}